package com.tool.log.example.model;

public class Child {

    private int id;
    private String name;

    public Child() {

    }

    public Child(int id, String name) {
        this.id = id;
        this.name = name;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
